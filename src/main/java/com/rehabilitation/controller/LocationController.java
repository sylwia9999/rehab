package com.rehabilitation.controller;

import com.rehabilitation.Dto.LocationResponse;
import com.rehabilitation.service.LocationService;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.CrossOrigin;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RequestParam;
import org.springframework.web.bind.annotation.RestController;

import java.util.List;

@RestController
@CrossOrigin("http://localhost:4200")
public class LocationController {
    private final LocationService locationService;

    @Autowired
    public LocationController(LocationService locationService) {
        this.locationService = locationService;
    }

    @GetMapping("/rehab/locations")
    public ResponseEntity<List<LocationResponse>> getAll() {
        return new ResponseEntity<>(locationService.getAll(), HttpStatus.OK);
    }

}
